    ('SCM','Source Code Management repository'),
    ('Presentation','Presentation document'),
    ('Meeting','External meeting link'),
    ('Demonstration','Where a running version of the team project can be viewed'),
    ('Video','Video (YouTube, Vimeo, etc.)'),
    ('Documentation','Documentation for the team project'),
    ('Spotify Playlist', 'Spotify playlist')
;

